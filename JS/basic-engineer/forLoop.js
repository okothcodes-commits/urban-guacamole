/*For loop is meant for numbers
It's a combination of using while for numbers

A while loop asks, “Should I keep going?”
A for loop says, “I’m going to count from here to there.”

*/

let k = 35
while (k<20){
    console.log(`k, ${k}`);
    k = k-1;
}

//for loop
for(let k=35; k<20; k-1){
    console.log(`k, ${k}`)
}

