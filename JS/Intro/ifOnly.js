let age=prompt("Insert your age")
alert(`You added ${age}`)

if(age<=10){
    alert(`Its Time to Play!`)
}

if(age>10 && age<=13){
    alert(`How is teenage life so far?`)
}

if(age>13 && age<=18){
    alert(`Welcome to adulthood!`)
}

if(age>18 && age<=25){
    alert(`I hope you're living your best life`)
}

if(age>25 && age<=35){
    alert(`You are exactly what you think you are!`)
}

if(age>=36){
    alert(`You are exactly where you need to be`)
}